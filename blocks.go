package main

import "strings"

// ----------------------------------------------------------------------------------------------------------------
// Splits raw strings into blocks.
// ----------------------------------------------------------------------------------------------------------------

type Blocks struct {
	raw    string
	blocks []string
}

// ----------------------------------------------------------------------------------------------------------------
// Object creation
// ----------------------------------------------------------------------------------------------------------------

func NewBlocks(rawInput *string) *Blocks {
	blocks := &Blocks{
		raw: *rawInput,
	}
	blocks.create()

	return blocks
}

// ----------------------------------------------------------------------------------------------------------------
// Methods.
// ----------------------------------------------------------------------------------------------------------------

func (b *Blocks) create() {
	b.blocks = strings.Split(b.raw, "\n\n")
}

// ----------------------------------------------------------------------------------------------------------------

func (b *Blocks) getBlocks() *[]string {
	return &b.blocks
}

